package de.tum.in.net.analysis;

public class NetworkStats {

  private int countTotal;
  private float interceptionRateTotal;


  public int getCountTotal() {
    return countTotal;
  }

  public void setCountTotal(int countTotal) {
    this.countTotal = countTotal;
  }

  public float getInterceptionRateTotal() {
    return interceptionRateTotal;
  }

  public void setInterceptionRateTotal(float interceptionRateTotal) {
    this.interceptionRateTotal = interceptionRateTotal;
  }

}
